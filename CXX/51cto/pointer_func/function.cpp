#include "function.h"

namespace funcinfo {

double add(double value1, double value2) {
    return value1 + value2;
}

double mul(double value1, double value2) {
    return value1 * value2;
}

double div(double value1, double value2) {
    return value1 / value2;
}

double squre(double value1) {
    return value1 * value1;
}

}